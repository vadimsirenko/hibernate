drop table if exists Item;
drop table if exists Passport;
drop table if exists Person;

create table Person(
                       id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
                       name varchar(30) NOT NULL,
                       age int CHECK(age>0)
);


